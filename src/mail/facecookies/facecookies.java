    public class HiloCorreo extends Thread {

        public volatile boolean estado = true;

        @Override
        public void run() {
            System.out.println("Iniciar Escucha....!!!");
            while (estado) {
                // Preguntar si hay mail
                String content = POP_Cliente.readMail();
                if (content != null) {
                    System.out.println("Llego Correo....!!!");
                    new HiloAtencion(content).start();
                }
                waitCiclo();
            }
            System.out.println("Terminar Escucha....!!!");
        }

        public void waitCiclo() {
            try {
                sleep(Constantes.TIME_THREAD * 1000); // Esperar 2 seg.
            } catch (InterruptedException ex) {
                Logger.getLogger(facecookies.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    
    public class HiloAtencion extends Thread {

        public volatile String mensaje;

        public HiloAtencion(String mensaje) {
            this.mensaje = mensaje;
        }

        @Override
        public void run() {
            try {
                // debo ir a crear el metodo processMessage
                new FacecookiesMail().processMessage(mensaje);
            } catch (MessagingException ex) {
                Logger.getLogger(facecookies.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
